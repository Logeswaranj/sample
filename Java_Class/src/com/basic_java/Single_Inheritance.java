package com.basic_java;

public class Single_Inheritance {
	/*
	 * INHERITANCE - Accessing one class property into another class using
	 * " extends " keyword , relationship between parent class and child class
	 * object and main method created in child class
	 * SINGLE INHERITANCE - One parent class is accessed by one child class
	 */

	public void friends_List() {
		System.out.println("My friends list ");

	}

	public void friend_One() {
		System.out.println("1. Prabhu");

	}

	public void friend_Two() {
		System.out.println("2. Susi");

	}

	public void friend_Three() {
		System.out.println("3. Gopi");

	}
}
