package com.basic_java;

public class Multilevel_Inheritance_Parent {
	/*MULTILEVEL INHERITANCE - One parent class is accessed by one child class which 
	 * is being accessed by another child class 
	 */
public void university_List() {
	System.out.println("University List");
	
}

public void first_University() {
	System.out.println("University of Oxford");
	
}
public void second_University() {
	System.out.println("University of Cambridge");
	
}
public void thired_University() {
	System.out.println("Stanford University");
	
}
}
